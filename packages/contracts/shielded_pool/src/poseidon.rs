fn fr_from_array(env: &Env, bytes: &[u8; 32]) -> Fr {
    Fr::from_bytes(BytesN::from_array(env, bytes))
}

fn zero_fr(env: &Env) -> Fr {
    fr_from_array(env, &[0u8; 32])
}

fn fr_pow5(value: &Fr) -> Fr {
    let squared = value.clone() * value.clone();
    let fourth = squared.clone() * squared;
    fourth * value.clone()
}

fn round_constant(env: &Env, round: usize, column: usize) -> Fr {
    fr_from_array(env, &POSEIDON_T3_CONSTANTS[round * 3 + column])
}

fn matrix_constant(env: &Env, row: usize, column: usize) -> Fr {
    fr_from_array(env, &POSEIDON_T3_MATRIX[row * 3 + column])
}

fn apply_mds(env: &Env, s0: Fr, s1: Fr, s2: Fr) -> (Fr, Fr, Fr) {
    let n0 = s0.clone() * matrix_constant(env, 0, 0)
        + s1.clone() * matrix_constant(env, 0, 1)
        + s2.clone() * matrix_constant(env, 0, 2);
    let n1 = s0.clone() * matrix_constant(env, 1, 0)
        + s1.clone() * matrix_constant(env, 1, 1)
        + s2.clone() * matrix_constant(env, 1, 2);
    let n2 = s0 * matrix_constant(env, 2, 0)
        + s1 * matrix_constant(env, 2, 1)
        + s2 * matrix_constant(env, 2, 2);
    (n0, n1, n2)
}

fn poseidon_hash2(env: &Env, left: &BytesN<32>, right: &BytesN<32>) -> BytesN<32> {
    let mut s0 = zero_fr(env);
    let mut s1 = Fr::from_bytes(left.clone());
    let mut s2 = Fr::from_bytes(right.clone());

    for round in 0..POSEIDON_T3_ROUNDS {
        s0 = s0 + round_constant(env, round, 0);
        s1 = s1 + round_constant(env, round, 1);
        s2 = s2 + round_constant(env, round, 2);

        if round < POSEIDON_T3_FULL_ROUNDS_HALF
            || round >= POSEIDON_T3_FULL_ROUNDS_HALF + POSEIDON_T3_PARTIAL_ROUNDS
        {
            s0 = fr_pow5(&s0);
            s1 = fr_pow5(&s1);
            s2 = fr_pow5(&s2);
        } else {
            s0 = fr_pow5(&s0);
        }

        let next = apply_mds(env, s0, s1, s2);
        s0 = next.0;
        s1 = next.1;
        s2 = next.2;
    }

    s0.to_bytes()
}

fn zero_hashes(env: &Env) -> Vec<BytesN<32>> {
    let mut zeros = Vec::new(env);
    let mut current = BytesN::from_array(env, &[0u8; 32]);
    zeros.push_back(current.clone());
    for _ in 0..TREE_DEPTH {
        current = poseidon_hash2(env, &current, &current);
        zeros.push_back(current.clone());
    }
    zeros
}

pub fn compute_root_from_commitments(env: &Env, leaves: &Vec<BytesN<32>>) -> BytesN<32> {
    let zeros = zero_hashes(env);
    if leaves.len() == 0 {
        return zeros.get(TREE_DEPTH).unwrap();
    }

    let mut level: Vec<BytesN<32>> = Vec::new(env);
    for leaf in leaves.iter() {
        level.push_back(leaf);
    }

    for depth in 0..TREE_DEPTH {
        if level.len() % 2 != 0 {
            level.push_back(zeros.get(depth).unwrap());
        }

        let mut next: Vec<BytesN<32>> = Vec::new(env);
        let mut index = 0;
        while index < level.len() {
            let left = level.get(index).unwrap();
            let right = level.get(index + 1).unwrap();
            next.push_back(poseidon_hash2(env, &left, &right));
            index += 2;
        }
        level = next;
    }

    level.get(0).unwrap()
}

